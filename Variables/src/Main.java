public class Main {
    public static void main(String[] args) {
        //Declaring the variable
        int number;
        //Giving a value to our variable
        number = 10;
        System.out.println(number);

        //Declaring the variable at the same time we give a value
        int salary = 1000;
        String employeeName = "Héctor Velázquez";
        //Printing both variables
        System.out.println("The employee " + employeeName + " has a salary of " + salary);
    }
}